fun countFences(input: String): Long {
    val plots = input.split("\n")
        .map { it.toCharArray().toList() }
        .mapIndexed { y, row -> row.mapIndexed { x, c -> GardenPlot(x, y, c, -1) } }
        .flatten()
        .map { (it.x to it.y) to it }
        .toMap()

    plots.forEach { populateRegions(it.value, plots) }

    val regions = plots.values.groupBy { it.region }

    // println(regions)

    return regions.map {
        val res = it.value.sumOf { plot -> plot.perimeter(plots) } * it.value.sumOf { plot -> plot.area() }
        // println("Region ${it.key} crop ${it.value.first().char} has area ${it.value.sumOf { it.area() }} and perimeter ${it.value.sumOf { it.perimeter(plots) }}, cost $res")
        res
    }.sum().toLong()
}

fun countFencesWithSides(input: String): Long {
    val plots = input.split("\n")
        .map { it.toCharArray().toList() }
        .mapIndexed { y, row -> row.mapIndexed { x, c -> GardenPlot(x, y, c, -1) } }
        .flatten()
        .map { (it.x to it.y) to it }
        .toMap()

    plots.forEach { populateRegions(it.value, plots) }

    val regions = plots.values.groupBy { it.region }

    // println(regions)

    return regions.map {
        val sides = it.value.sumOf { plot -> plot.sides(it.value) }
        val res = it.value.sumOf { plot -> plot.area() } * sides
        println(
            "Region ${it.key} crop ${it.value.first().char} has area ${it.value.sumOf { it.area() }} and sides $sides, cost $res"
        )
        res
    }.sum().toLong()
}

private var lastRegion = 0

fun populateRegions(plot: GardenPlot, plots: Map<Pair<Int, Int>, GardenPlot>) {
    if (plot.region != -1) return
    plot.region = lastRegion++
    populateNeighbours(plot, plots)
}

fun populateNeighbours(plot: GardenPlot, plots: Map<Pair<Int, Int>, GardenPlot>) {
    val up = plot.up()
    val down = plot.down()
    val left = plot.left()
    val right = plot.right()

    val neighbours = listOf(up, down, left, right)
        .mapNotNull { plots[it] }
        .filter { it.char == plot.char && it.region == -1 }

    neighbours.forEach {
        it.region = plot.region
        populateNeighbours(it, plots)
    }
}

data class GardenPlot(val x: Int, val y: Int, val char: Char, var region: Int) {
    fun up(): Pair<Int, Int> = x to y - 1
    fun down(): Pair<Int, Int> = x to y + 1
    fun left(): Pair<Int, Int> = x - 1 to y
    fun right(): Pair<Int, Int> = x + 1 to y
    fun upLeft(): Pair<Int, Int> = x - 1 to y - 1
    fun upRight(): Pair<Int, Int> = x + 1 to y - 1
    fun downLeft(): Pair<Int, Int> = x - 1 to y + 1
    fun downRight(): Pair<Int, Int> = x + 1 to y + 1

    fun r(region: List<GardenPlot>): Boolean = region.any { it.x == right().first && it.y == right().second }
    fun l(region: List<GardenPlot>): Boolean = region.any { it.x == left().first && it.y == left().second }
    fun u(region: List<GardenPlot>): Boolean = region.any { it.x == up().first && it.y == up().second }
    fun d(region: List<GardenPlot>): Boolean = region.any { it.x == down().first && it.y == down().second }
    fun ul(region: List<GardenPlot>): Boolean = region.any { it.x == upLeft().first && it.y == upLeft().second }
    fun ur(region: List<GardenPlot>): Boolean = region.any { it.x == upRight().first && it.y == upRight().second }
    fun dl(region: List<GardenPlot>): Boolean = region.any { it.x == downLeft().first && it.y == downLeft().second }
    fun dr(region: List<GardenPlot>): Boolean = region.any { it.x == downRight().first && it.y == downRight().second }

    fun area(): Int = 1
    fun perimeter(plots: Map<Pair<Int, Int>, GardenPlot>): Int = 4 - neighbours(plots).size

    private fun neighbours(plots: Map<Pair<Int, Int>, GardenPlot>) =
        listOf(up(), down(), left(), right())
            .mapNotNull { plots[it] }
            .filter { it.region == region }

    fun sides(region: List<GardenPlot>): Int {
        // println(this)
        var res = 0
        // outer top right corner
        if (!r(region) && !u(region)) res += 1
        // outer top left corner
        if (!l(region) && !u(region)) res += 1
        // outer bottom right corner
        if (!r(region) && !d(region)) res += 1
        // outer bottom left corner
        if (!l(region) && !d(region)) res += 1

        // inner bottom right corner
        if (r(region) && d(region) && !dr(region)) res += 1
        // inner bottom left corner
        if (l(region) && d(region) && !dl(region)) res += 1
        // inner top right corner
        if (r(region) && u(region) && !ur(region)) res += 1
        // inner top left corner
        if (l(region) && u(region) && !ul(region)) res += 1

        // println(res)
        return res
    }
}

